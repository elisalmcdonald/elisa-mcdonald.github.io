////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if (num1 > num2) {
    return num2
  } else {
    return num1
  }

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {
  if (number > 1) {
    return 1
  }
  return isEven(number - 2)
  }


////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
    var count = 0
    for (var i = 0; i < string.length; i++) {
      if (string[i] === char) {
        count++
      }
    } return count
  }

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var letterB = 0
  for (var i = 0; i < string.length; i++) {
    if (string[i] === 'B') {
      letterB++
    }
  } return letterB
}

